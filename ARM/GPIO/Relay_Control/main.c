//Program to switch off and switch on a relay
#include "LPC214x.H"                       /* LPC214x definitions */
#include "delay.h"

/*****************************************************************************
**   Main Function  main()
******************************************************************************/
int main (void)
{
	IO0DIR |= ((1<<22) | (1<<31));	// pin: p0.22 & p0.31

	while(1)
	{
		delay_mSec(1000);
		IO0SET |= (1<<22);
		delay_mSec(1000);
		IO0CLR |= (1<<22);

		delay_mSec(1000);
		IO0SET |= (1<<31);
		delay_mSec(1000);
		IO0CLR |= (1<<31);
	}
//    return 0;
}

